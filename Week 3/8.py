# for loop and range:

for i in range(5): # the variable i will take the values from the range 5 i.e 0-4
    print(i,'hey')
# 0 hey
# 1 hey
# 2 hey
# 3 hey
# 4 hey

''' range(5) represents the following sequence: 0, 1, 2, 3, 4. In general, range(n) represents
the sequence: 0, 1, ..., n - 1. '''